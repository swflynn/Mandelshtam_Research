! If you wish to inclue a module, it should go at the beginning of your program. 
! You can also have seperate files be modules, Vlad prefers at the top. 
! You must define the module (environment) and give it a name
! Any global variables need to be defined before any subroutines/functions

!=====================================================================================!
!==============================Define My Module=======================================!
!=====================================================================================!
MODULE tutorial_mod
      IMPLICIT NONE
!=====================================================================================!
!==============================Define My Global Variables=============================!
!=====================================================================================!
      INTEGER :: ans
!=====================================================================================!
!==============================Define My Subroutines/Functions========================!
!=====================================================================================!
CONTAINS

  SUBROUTINE addition(i1,i2)
      IMPLICIT NONE
      INTEGER :: i1, i2
      ans = i1 + i2
  END SUBROUTINE addition

END MODULE tutorial_mod
! Notice wen you compile the code it generates tutorial_mod.mod
! this file is the binary of your module and is necessary to run the code
!=====================================================================================!
!==============================Define My Program======================================!
!=====================================================================================!
PROGRAM tutorial_pgrm
!=====================================================================================!
!==============================Define What Modules To Use=============================!
!=====================================================================================!
USE tutorial_mod

  IMPLICIT NONE
  INTEGER :: a, b
!=====================================================================================!
!==============================Read in Input File=====================================!
!=====================================================================================!
  OPEN(20,FILE='input.dat')
  READ(20,*) a
  READ(20,*) b

  WRITE(*,*) 'This is your first number', a
  WRITE(*,*) 'This is your second number', b
  WRITE(*,*) 'Check 1 Complete: Read in Data'
!=====================================================================================!
!==============================Check 1================================================!
!=====================================================================================!
  CALL addition(a,b)
  WRITE(*,*) 'this is your answer'
  WRITE(*,*) ans
  WRITE(*,*) 'Check 2 Complete: Call Subroutine addition'
!=====================================================================================!
!==============================Check 2================================================!
!=====================================================================================!

END PROGRAM tutorial_pgrm
